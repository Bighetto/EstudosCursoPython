# Functions (Funções)
# Parametro (Argumento)

def boas_vindas (nome, quantidade):
    print (f'Olá {nome}.')
    print (f'Temos {str (quantidade)} laptops em estoque.')


boas_vindas ('Matheus', 5)
boas_vindas ('Arthur', 3)
boas_vindas ('Fernando', 1)

boas_vindas

